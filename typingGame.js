const selectedTime = document.getElementById("time-select");
const wordDisplay = document.getElementById("word-display");
const inputArea = document.getElementById("input-area");
const timeEle = document.getElementById("time");
const accuracyEle = document.getElementById("accuracy");
const wpmEle = document.getElementById("wpm");
const mistakesEle = document.getElementById("Mistakes");

let timeLeft = parseInt(selectedTime.value);
let Accuracy = 0;
let wpm = 0;
let timerInterval;
let timerInterval2;
let totalWordsTyped = 0;
let correctChar = 0;
let totalCharTyped = 0;
let gameStarted = false;
let count = 0;
let lenOfPara = 0;
let paragraph = "";
let mistakes = 0;
let timeConsume = 0;
let data = {
    dwpm: 0,
    daccuracy: 0,
    dmistakes: 0,
    dtime: 0,
    dcorrectCount: 0,
    dlenofpara: 0,
    dtimeconsume: 0
};

timeEle.innerText = `Time: ${timeLeft}s`;
wpmEle.innerText = `WPM: ${wpm}`;
accuracyEle.innerText = `Accuracy: ${Accuracy}%`;
mistakesEle.innerText = `Mistakes: ${mistakes}`;

function loadePara() {
    let randomText = Math.floor(Math.random() * texts.length);
    wordDisplay.innerText = '';
    paragraph = '';
    lenOfPara = 0;
    texts[randomText].split('').forEach((char) => {
        paragraph += char;
        let span = `<span id="textSpan${lenOfPara}">${char}</span>`;
        wordDisplay.innerHTML += span;
        lenOfPara++;
    });
    data.dlenofpara = lenOfPara;
    document.addEventListener("keydown", () => inputArea.focus());
    inputArea.addEventListener("click", () => inputArea.focus());
}

function calculateWPM() {
    const wordsTyped = inputArea.value.trim().split(/\s+/).filter(word => word.length > 0).length;
    const minutes = (selectedTime.value - timeLeft) / 60;

    if (minutes > 0) {
        return Math.floor(wordsTyped / minutes);
    } else {
        return 0;
    }
}

function calculateAccuracy() {
    let inputLetters = inputArea.value.split('');
    let targetText = paragraph.split('');
    let correctCount = 0;

    inputLetters.forEach((char, index) => {
        if (char === targetText[index]) {
            correctCount++;
        }
    });
    if (inputLetters.length === 0) {
        return 100;
    }
    return Math.floor((correctCount / inputLetters.length) * 100);
}

function startTimer() {
    timeLeft = parseInt(selectedTime.value); // Get the selected time
    timerInterval = setInterval(() => {
        timeLeft--;
        timeEle.innerText = `Time: ${timeLeft}s`;
        if (timeLeft === 0) {
            clearInterval(timerInterval);
            inputArea.disabled = true;
            resultDisplay();
        }
    }, 1000);
}

function TotalTimeConsume() {
    timerInterval2 = setInterval(() => {
        timeConsume++;
        if (timeLeft === timeConsume) {
            clearInterval(timerInterval2);
        }
    }, 1000);
    return timeConsume;
}

function checkInput() {
    let textSpan = document.getElementById(`textSpan${count}`).innerText;
    let inputText = inputArea.value.split("")[count];
    data.dtimeconsume = TotalTimeConsume();
    if (correctChar === lenOfPara) {
        resultDisplay();
        clearInterval(timerInterval); 
        clearInterval(timerInterval2); 
    }
    if (inputText != null) {
        if (inputText === textSpan) {
            document.getElementById(`textSpan${count}`).style.color = "lightgreen";
            correctChar++;
        } else {
            document.getElementById(`textSpan${count}`).style.color = "rgb(249, 76, 76) ";
            mistakes++;
        }
    }

    if (inputArea.value.length < count) {
        count--;
        if (document.getElementById(`textSpan${count}`).style.color === "red") {
            mistakes--;
        }
        document.getElementById(`textSpan${count}`).style.color = "rgba(241, 239, 239, 0.276)";
    } else {
        count++;
    }
    wpm = calculateWPM();
    data.dwpm = wpm;
    Accuracy = calculateAccuracy();
    data.daccuracy = Accuracy;
    data.dmistakes = mistakes;
    data.dcorrectCount = correctChar;
    wpmEle.innerText = `WPM: ${wpm}`;
    accuracyEle.innerText = `Accuracy: ${Accuracy}%`;
    mistakesEle.innerText = `Mistakes: ${mistakes}`;
}

function resetGame() {
    clearInterval(timerInterval); 
    clearInterval(timerInterval2);
    timeLeft = parseInt(selectedTime.value); 
    mistakes = 0;
    Accuracy = 0;
    wpm = 0;
    correctChar = 0;
    gameStarted = false;
    inputArea.value = "";
    inputArea.disabled = false;
    count = 0;
    lenOfPara = 0;
    paragraph = "";
    timeEle.innerText = `Time: ${timeLeft}s`;
    wpmEle.innerText = `WPM: ${wpm}`;
    accuracyEle.innerText = `Accuracy: ${Accuracy}%`;
    mistakesEle.innerText = `Mistakes: ${mistakes}`;
    wordDisplay.innerHTML = "";
    loadePara();
    resultDisplayRemove();
}
const result = document.createElement('div');
result.classList.add("result");

function resultDisplay() {
    result.innerHTML = `<h3> Keep trying !</h3>
                    <pre>WPM                   ${data.dwpm}</pre>
                    <pre>Accuracy              ${data.daccuracy}%</pre>
                    <pre>Mistakes              ${data.dmistakes}</pre>
                    <pre>Corrects              ${data.dcorrectCount}</pre>
                    <pre>Total words           ${data.dlenofpara}</pre>
                    `;
                    
    document.getElementsByTagName("h1")[0].after(result);
    
    const resultElementX = document.getElementsByClassName("result")[0];
    resultElementX.addEventListener("click", () => {
        resultElementX.remove("result"); 
    });
}

function resultDisplayRemove() {
    const resultElement = document.getElementsByClassName("result")[0]; 
    if (resultElement) {
        resultElement.remove(); 
    }
}

